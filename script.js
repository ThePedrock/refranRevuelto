  const firstPart = first.firstPart || "";
  const secondPart = second.secondPart || "";
  const firstId = first.id || 0;
  const secondId = second.id || 0;

  const newId = firstId * maxrange + secondId;
  const resultado = `${firstPart} ${secondPart}`;

  displayProverb(selectedId, first.id, second.id);
